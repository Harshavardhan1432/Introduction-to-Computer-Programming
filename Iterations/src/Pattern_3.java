public class Pattern_3
{
    public static void main(String [] args)
    {
        int i,j,k;
        for (i=1;i<=5;i++)
        {
            for (j = 1;j<=i;j++)
            {
                k=i;
                System.out.print(k);
            }
            System.out.println();
        }
    }
}
